def test_csv_to_isatab_dataset_loading(tmp_path: Path) -> None:
    """Test that generated ISA-Tab can be loaded as a DataSet."""
    csv_file = tmp_path / "samples.csv"
    with open(csv_file, "w", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(["sample_id", "organism", "reads_file"])
        writer.writerow(["S001", "Mus musculus", "S001_R1.fastq.gz"])
        writer.writerow(["S002", "Mus musculus", "S002_R1.fastq.gz"])
        writer.writerow(["S003", "Homo sapiens", "S003_R1.fastq.gz"])

    output_dir = tmp_path / "isatab"
    import asyncio
    asyncio.run(csv_to_isatab(csv_file, output_dir, config=None))

    # Load as DataSet
    dataset = load_dataset_from_isatab(output_dir, validate=False)

    # Verify dataset properties
    assert len(dataset.sample_metadata) == 3
    assert "S001" in dataset.sample_metadata
    assert "S002" in dataset.sample_metadata
    assert "S003" in dataset.sample_metadata

    # Organisms should be detected
    assert len(dataset.organisms) >= 1


