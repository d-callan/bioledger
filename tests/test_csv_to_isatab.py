def test_csv_to_isatab_study_file_content(tmp_path: Path) -> None:
    """Test that s_study.txt has correct structure."""
    csv_file = tmp_path / "samples.csv"
    with open(csv_file, "w", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(["sample_id", "organism"])
        writer.writerow(["S001", "Mus musculus"])
        writer.writerow(["S002", "Homo sapiens"])

    output_dir = tmp_path / "isatab"
    import asyncio
    asyncio.run(csv_to_isatab(csv_file, output_dir, config=None))

    # Read and verify s_study.txt structure
    study_file = output_dir / "s_study.txt"
    content = study_file.read_text()

    # Should have header with expected columns
    lines = content.strip().split("\n")
    assert len(lines) == 3  # header + 2 samples

    header = lines[0].split("\t")
    assert "Source Name" in header
    assert "Sample Name" in header

    # Each sample should have source and sample name
    for line in lines[1:]:
        parts = line.split("\t")
        assert len(parts) >= 2
        assert parts[0].startswith("source_")
        assert parts[-1] in ["S001", "S002"]
