"""Tests for CSV to ISA-Tab conversion in ISAForge."""
from __future__ import annotations

import csv
from pathlib import Path

import pytest

from bioledger.forges.isaforge.builder import csv_to_isatab
from bioledger.forges.isaforge.dataset import load_dataset_from_isatab
from bioledger.forges.isaforge.validate import validate_isatab


def test_csv_to_isatab_basic(tmp_path: Path) -> None:
    """Test basic CSV to ISA-Tab conversion without LLM config."""
    # Create a simple CSV samplesheet
    csv_file = tmp_path / "samples.csv"
    with open(csv_file, "w", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(["sample_id", "organism", "reads_file"])
        writer.writerow(["S001", "Mus musculus", "S001_R1.fastq.gz"])
        writer.writerow(["S002", "Homo sapiens", "S002_R1.fastq.gz"])

    # Convert to ISA-Tab (no config = fallback mode)
    output_dir = tmp_path / "isatab"
    import asyncio
    result = asyncio.run(csv_to_isatab(csv_file, output_dir, config=None))

    # Verify output directory exists
    assert result.exists()
    assert result == output_dir

    # Verify ISA-Tab files were created (a_assay.txt may not be generated by isatools)
    assert (output_dir / "i_investigation.txt").exists()
    assert (output_dir / "s_study.txt").exists()
    assert (output_dir / "samples.csv").exists()  # Original preserved


def test_csv_to_isatab_validation(tmp_path: Path) -> None:
    """Test that generated ISA-Tab passes validation."""
    csv_file = tmp_path / "samples.csv"
    with open(csv_file, "w", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(["sample_id", "organism", "data_file"])
        writer.writerow(["S001", "E. coli", "data.txt"])

    output_dir = tmp_path / "isatab"
    import asyncio
    asyncio.run(csv_to_isatab(csv_file, output_dir, config=None))

    # Validate the generated ISA-Tab
    result = validate_isatab(output_dir)
